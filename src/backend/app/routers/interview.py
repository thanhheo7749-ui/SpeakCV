async def end_interview(request: models.ReportRequest, current_user: sql_models.User = Depends(get_current_user), db: Session = Depends(get_db)):
    try:
        system_prompt = f"""
        Bạn là HR. Đánh giá ứng viên vị trí: {request.jd_text}.
        DỰA CHÍNH XÁC VÀO LỊCH SỬ PHỎNG VẤN. QUY TẮC TỐI THƯỢNG:
        1. BẮT BUỘC PHẢI TRÍCH XUẤT VÀ ĐÁNH GIÁ TẤT CẢ CÁC CÂU HỎI CỦA AI CÓ TRONG LỊCH SỬ. SỐ LƯỢNG OBJECT TRONG MẢNG `details` PHẢI BẰNG ĐÚNG SỐ LƯỢNG CÂU HỎI TRONG LỊCH SỬ! TUYỆT ĐỐI KHÔNG ĐƯỢC LƯỢC BỎ BẤT KỲ CÂU NÀO.
        2. TUYỆT ĐỐI KHÔNG tự bịa ra câu trả lời của ứng viên.
        3. PHÂN BIỆT RÕ RÀNG TRẠNG THÁI TRẢ LỜI: BẠN CHỈ ĐƯỢC PHÉP ghi 'Ứng viên chưa trả lời' vào `candidate_answer` nếu trong lịch sử AI vừa hỏi mà ứng viên chưa có câu trả lời nào bên dưới, hoặc ứng viên CỐ TÌNH BỎ QUA (dùng từ 'next', 'không biết', 'bỏ qua'). Không trừ điểm câu đó nếu bị ngắt do lỗi hệ thống.
        4. NẾU ỨNG VIÊN CÓ ĐƯA RA CÂU TRẢ LỜI: Cho dù câu trả lời đó sai kiến thức, thiếu ý, lan man, hay ngô nghê, BẠN BẮT BUỘC PHẢI trích dẫn nguyên văn câu nói đó vào trường `candidate_answer`. TUYỆT ĐỐI KHÔNG ĐƯỢC gán nhãn là 'Ứng viên chưa trả lời' trong trường hợp này.
        5. Đối với các câu trả lời sai/thiếu đó, hãy chấm điểm thấp và ghi rõ lý do sai vào trường `evaluation`.
        
        QUY TẮC QUAN TRỌNG VỀ LỊCH SỬ BỊ GIÁN ĐOẠN: 
        Nếu ứng viên trả lời một câu hỏi cũ đã từng bị bỏ qua hoặc bị ngắt quãng trước đó, bạn PHẢI tự động ghép nối câu trả lời mới đó vào đúng câu hỏi tương ứng. GHI ĐÈ lên trạng thái 'Ứng viên chưa trả lời'. TUYỆT ĐỐI KHÔNG tạo ra 2 bản ghi trùng lặp cho cùng 1 câu hỏi.
        
        BẮT BUỘC trả về JSON (không có markdown):
        {{
            "score": <điểm float 1-10>,
            "inferred_position": "<Dự đoán chức danh công việc dựa trên câu hỏi (dưới 5 từ). Nếu không rõ, ghi 'Tự do'>",
            "overall_feedback": "<Nhận xét tổng quan>",
            "details": [
                {{
                    "question": "<Câu AI hỏi>",
                    "candidate_answer": "<Trích dẫn ĐÚNG LỜI ứng viên>",
                    "evaluation": "<Nhận xét chi tiết>",
                    "ideal_answer": "<Câu trả lời mẫu>"
                }}
                ... (LẶP LẠI CHO TẤT CẢ CÁC CÂU HỎI TRONG LỊCH SỬ, CŨ LẪN MỚI)
            ]
        }}
        """
        
        messages = [{"role": "system", "content": system_prompt}, {"role": "user", "content": f"Lịch sử:\n{request.history}"}]
        
        raw_content = call_ai_chat(
            messages=messages,
            model="gpt-4o-mini",
            temperature=0.2,
            max_tokens=4000,
            response_format={"type": "json_object"},
            timeout=90
        )
        
        if raw_content:
            report_data = clean_and_parse_json(raw_content)
            score = report_data.get("score", 0)
            overall_feedback = report_data.get("overall_feedback", "")
            details = report_data.get("details", [])
            
            inferred_position = report_data.get("inferred_position", "").strip()
            
            final_position = request.position
            if not final_position or final_position.strip().lower() in ["", "chung", "tự do", "free", "none", "null"]:
                final_position = inferred_position if inferred_position else "Tự do"

            history_id = None
            if request.history_id is not None:
                existing = db.query(sql_models.InterviewHistory).filter(
                    sql_models.InterviewHistory.id == request.history_id,
                    sql_models.InterviewHistory.user_id == current_user.id
                ).first()
                if existing:
                    existing.score = score
                    existing.overall_feedback = overall_feedback
                    existing.details = details
                    
                    if not existing.position or existing.position.strip().lower() in ["", "chung", "tự do"]:
                        existing.position = final_position
                        existing.title = f"Phỏng vấn {final_position}"
                        
                    db.commit()
                    history_id = existing.id
            
            if history_id is None:
                title = f"Phỏng vấn {final_position}"
                new_history = sql_models.InterviewHistory(
                    user_id=current_user.id,
                    position=final_position,
                    title=title,
                    score=score,
                    overall_feedback=overall_feedback,
                    details=details,
                    interview_type=request.interview_type,
                    question_limit=request.question_limit,
                    time_limit=request.time_limit
                )
                db.add(new_history)
                db.commit()
                db.refresh(new_history)
                history_id = new_history.id
            else:
                existing.interview_type = request.interview_type
                existing.question_limit = request.question_limit
                existing.time_limit = request.time_limit
                db.commit()
            
            return {"report": report_data, "history_id": history_id}
        else:
            return {"report": {"score": 0, "overall_feedback": "AI Server error.", "details": []}}
            
    except Exception as e: 
        print(f"❌ Report error: {e}")
        return {"report": {"score": 0, "overall_feedback": "System error.", "details": []}}

@router.get("/api/history")
async def get_interview_history(current_user: sql_models.User = Depends(get_current_user), db: Session = Depends(get_db)):
    histories = db.query(sql_models.InterviewHistory).filter(sql_models.InterviewHistory.user_id == current_user.id).order_by(sql_models.InterviewHistory.created_at.desc()).all()
    return {"histories": histories}

