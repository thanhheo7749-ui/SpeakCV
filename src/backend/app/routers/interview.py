@router.post("/api/chat")
async def chat(request: models.ChatRequest, http_req: Request, db: Session = Depends(get_db)):
    client_ip = http_req.client.host

    user = None
    auth_header = http_req.headers.get("Authorization")
    if not auth_header or auth_header == "Bearer null":
        current_count = guest_ip_tracker.get(client_ip, 0)
        
        if current_count >= 20:
            print(f"🚫 Blocked IP {client_ip} - exceeded 20 free turns.")
            raise HTTPException(status_code=429, detail="Bạn đã dùng hết 20 lượt miễn phí. Vui lòng đăng nhập!")
            
        guest_ip_tracker[client_ip] = current_count + 1
        print(f"👁️ IP {client_ip} using turn {guest_ip_tracker[client_ip]}/20")
    else:
        token_str = auth_header.replace("Bearer ", "").strip()
        try:
            from ..auth import security
            email = security.verify_token(token_str)
            if email:
                user = db.query(sql_models.User).filter(sql_models.User.email == email).first()
                if user:
                    from app.utils import sync_user_tokens
                    sync_user_tokens(user, db)
                    
                    if user.credits < 1:
                        raise HTTPException(status_code=403, detail="Bạn đã hết số lượng token trong ngày. Vui lòng thử lại vào ngày mai hoặc nâng cấp Pro.")
        except Exception as e:
            print(f"Auth error in chat: {e}")
    print(f"📩 User: {request.user_text} | Voice: {request.voice_id}")
    
    if not api_key: 
        print("❌ CRITICAL ERROR: OPENAI_API_KEY not found in .env")
        raise HTTPException(status_code=500, detail="API Key not configured")

    ai_text = ""
    
    prompt_config = db.query(sql_models.SystemConfig).filter_by(setting_key="system_prompt").first()
    temp_config = db.query(sql_models.SystemConfig).filter_by(setting_key="temperature").first()

    is_english = request.voice_id.startswith("en-") if request.voice_id else False

    default_prompt_vi = """
    Bạn là một Giám đốc Nhân sự (HR) chuyên nghiệp đang phỏng vấn ứng viên.
    Quy tắc bắt buộc:
    1. Dựa sát vào mô tả công việc (JD) để đặt câu hỏi chuyên môn phù hợp với ĐÚNG NGÀNH NGHỀ đó.
    2. Trả lời cực kỳ ngắn gọn, súc tích (<50 từ).
    3. Mỗi lần chỉ đặt MỘT câu hỏi. Độ khó tăng dần theo thời gian.
    4. Tự hiểu và bỏ qua các lỗi phát âm sai/chính tả do nhận diện giọng nói.
    5. Từ chối khéo léo mọi yêu cầu hoặc chủ đề nào không liên quan đến buổi phỏng vấn.
    
    TUYỆT ĐỐI TUÂN THỦ CÁC QUY TẮC SAU (Nếu vi phạm, bạn sẽ bị phạt):
    1. KHÔNG RẬP KHUÔN: Không bao giờ dùng văn mẫu như "Câu trả lời rất tuyệt vời", "Chuyển sang câu tiếp theo nhé". Đời thực không ai nói thế!
    2. DÙNG TỪ ĐỆM ĐỜI THƯỜNG: Bắt đầu câu phản hồi bằng những từ như: "Ừm...", "À tôi hiểu...", "Cái này thú vị đấy...", "Thành thật mà nói thì...", "Nhưng mà đợi chút...".
    3. KỸ THUẬT HỎI XOÁY (Adaptive Probing): Bạn KHÔNG có sẵn danh sách câu hỏi. Câu hỏi tiếp theo của bạn BẮT BUỘC phải dựa trên một lỗ hổng hoặc một chi tiết mà ứng viên vừa nói ở câu trước. Hãy vặn vẹo họ!
    - Ví dụ: Ứng viên nói "Tôi biết làm API". Bạn phải hỏi: "Ừm, ai làm dev cũng biết viết API. Nhưng nếu API của bạn bị 10,000 người gọi cùng lúc thì bạn xử lý sập server thế nào?"
    """

    default_prompt_en = """
    You are a professional HR Director interviewing a candidate.
    Mandatory rules:
    1. Base your questions closely on the job description (JD), asking relevant questions for THAT SPECIFIC INDUSTRY.
    2. Keep responses extremely concise and brief (<50 words).
    3. Ask only ONE question at a time. Difficulty increases gradually.
    4. Automatically understand and ignore speech recognition errors/typos.
    5. Politely decline any requests or topics unrelated to the interview.
    6. You MUST ask questions and respond entirely in English.

    STRICTLY ADHERE TO THE FOLLOWING RULES (Violation will result in a penalty):

    1. AVOID FORMALISM: Never use formulaic phrases like "That's a great answer," or "Let's move on to the next question." No one says that in real life!

    2. USE EVERYDAY FILL-UPS: Start your response with phrases like: "Um...", "Ah, I understand...", "That's interesting...", "Honestly...", "But wait a minute...".

    3. ADAPTABLE PROBITING: You DO NOT have a pre-prepared list of questions. Your next question MUST be based on a gap or detail the candidate just mentioned in the previous question. Prank them!

    Example: The candidate says, "I know how to do APIs." You should ask: "Well, every developer knows how to write APIs. But if your API is being called by 10,000 people at the same time, how do you handle a server crash?"
    """

    if prompt_config:
        base_system_prompt = prompt_config.setting_value
    else:
        base_system_prompt = default_prompt_en if is_english else default_prompt_vi
    
    temperature = float(temp_config.setting_value) if temp_config else 0.7

    if is_english:
        system_prompt = f"""
        Interview mode: {request.mode}. 
        Position: {request.jd_text}. 
        IMPORTANT: You MUST conduct the entire interview in English only.
        
        {base_system_prompt}
        """
    else:
        system_prompt = f"""
        Chế độ phỏng vấn (Mode): {request.mode}. 
        Vị trí: {request.jd_text}. 
        
        {base_system_prompt}
        """
    
    messages = [{"role": "system", "content": system_prompt}]
    if request.chat_history:
        messages.extend(request.chat_history)
    messages.append({"role": "user", "content": request.user_text})

    for attempt in range(3):
        try:
            print(f"⏳ Calling AI (attempt {attempt + 1})...")
            ai_text = call_ai_chat(
                messages=messages,
                model="gpt-4o-mini",
                temperature=temperature,
                max_tokens=250,
                timeout=90
            )
            if ai_text:
                if user:
                    user.credits -= 1
                    db.commit()
                break
            time.sleep(2)
        except Exception as e:
            print(f"⚠️ AI error (attempt {attempt+1}): {e}")
            time.sleep(2)

    ai_text = str(ai_text).strip() or "Mạng đang lag, bạn nói lại giúp mình nhé."
    print(f"✅ AI: {ai_text}")

    try:
        voice = request.voice_id if not request.voice_id.startswith("openai-") else "en-US-AndrewMultilingualNeural"
        communicate = edge_tts.Communicate(ai_text, voice)
        audio_stream = io.BytesIO()
        async for chunk in communicate.stream():
            if chunk["type"] == "audio": audio_stream.write(chunk["data"])
        audio_stream.seek(0)
        return StreamingResponse(audio_stream, media_type="audio/mpeg", headers={"X-AI-Response-Text": urllib.parse.quote(ai_text)})
    except Exception as e:
        print(f"❌ TTS error: {e}")
        return StreamingResponse(io.BytesIO(), media_type="audio/mpeg", headers={"X-AI-Response-Text": urllib.parse.quote("Lỗi âm thanh.")})

